(ns ai.brainyard.display-block.core.eval
  "Convenience factories for eval-related display-blocks.

   Each fn is a thin wrapper over `text-block` that bakes in the class,
   class-dir, and section label appropriate for one of the sections
   produced by `format-eval-section` in `agent/tui/format.clj`:

     - eval-code-block    — Code section (Clojure / Bash / Python …)
     - eval-result-block  — Result section (return value)
     - eval-output-block  — Output section (captured stdout)
     - eval-error-block   — Error section (caught exceptions)

   All four share the same shape as `text-block`: pass `content` and
   optional `opts`; defaults are merged under caller opts so anything
   the caller supplies wins."
  (:require [ai.brainyard.display-block.core.text :as text]
            [clojure.string :as str]))

(defn- lang-label
  "Derive the default label for a code block given an optional :lang.

   Returns \"Code\" when lang is nil/blank, otherwise \"Code (<lang>)\"
   with lang lower-cased."
  [lang]
  (let [s (some-> lang name str/lower-case str/trim)]
    (if (str/blank? s)
      "Code"
      (str "Code (" s ")"))))

(defn- eval-section-block
  "Shared helper: a `text-block` pre-configured with a section's class,
   class-dir, and default label. Caller opts override the defaults."
  [content class class-dir default-label opts]
  (let [opts' (-> {:class     class
                   :storage   :file
                   :file-opts {:class-dir class-dir}
                   :label     default-label}
                  (merge opts))]
    (text/text-block content opts')))

(defn eval-code-block
  "Wrap `content` (a code string) as a display-block, using defaults
   suited for code generated by an LLM (Clojure / Bash / Python / …).

   Same shape as `text-block`. Defaults applied (overridable by `opts`):
     :class      :eval-code
     :storage    :file
     :file-opts  {:class-dir \"eval-code\"}
     :label      \"Code\" — or \"Code (<lang>)\" when `:lang` is supplied
                 and `:label` is not.

   Code-specific option:
     :lang  keyword or string (e.g. :clojure, :bash, :python).
            Drives the default :label so the expanded section header
            (`--- Expanded [Code (clojure)] (see …) ---`) and any
            consumer that reads `:label` from block metadata can show
            what kind of code is in the block. Ignored when `:label`
            is supplied explicitly.

   All other `text-block` opts (:max-collapsed-lines, :max-expanded-lines, :id,
   :hint, :storage, :file-opts) pass through unchanged."
  ([content] (eval-code-block content {}))
  ([content opts]
   (eval-section-block content :eval-code "eval-code"
                       (lang-label (:lang opts))
                       (dissoc opts :lang))))

