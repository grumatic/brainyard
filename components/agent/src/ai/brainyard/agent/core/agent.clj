(defn setup-agent
  "Create/retrieve an agent from options. Returns the started Agent record.
   Does NOT call ask/ask-async — caller controls dispatch.

   Reads all configuration from the options map. When called via deftool wrapper,
   defagent metadata is automatically merged into options.

   Key options (typically from defagent metadata):
     :id             - agent id keyword. Must be globally unique; should follow
                       the :<defagent-type>/<suffix> convention. If omitted,
                       one is auto-generated via `generate-instance-id`.
     :bt-factory     - (fn [options] bt-config)
     :agent-tools    - tool map to bind, or nil
     :instruction    - system prompt
     :tool-context   - tool usage context
     :agent-context  - additional context
     :max-iterations - max BT loop iterations

   Runtime options:
     :agent-session   - {:user-id :session-id} (required) — the agent-session
                        scope; sub-agents inherit this from :parent-agent's session.
     :parent-agent    - parent agent (if sub-agent call)
     :memory-opts     - memory manager options
     :config-extra    - map of agent-level defaults. Keys are split at
                        setup: schema-shape keys (in config-schema) flow
                        into st-memory-init :config (the per-agent
                        override layer read by config/get-config);
                        non-schema keys (e.g. :permissions) flow into
                        the agent's @!state :config metadata slot.
     :st-memory-extra - map merged into :st-memory-init
     :session-store   - custom session store

   Schema-key options at the top level of `options` (e.g. :eval-lm,
   :max-refinements) are extracted into st-memory-init :config and win
   over defagent author defaults supplied via :config-extra. Read them
   back via (config/get-config agent :key)."
  [& {:keys [id bt-factory agent-tools instruction tool-context
             agent-context max-iterations parent-agent
             config-extra st-memory-extra session-store
             _deftool$id _deftool$description]
      {:keys [user-id session-id]} :agent-session
      :as options}]
  (let [;; Resolve instance-id: explicit :id wins; otherwise derive from the
        ;; defagent's registered id (carried by the deftool wrapper as
        ;; :_deftool$id). Without this fallback, callers reaching setup-agent
        ;; via invoke-tool would land here with no id and produce an Agent
        ;; record with :agent-id nil, which crashes downstream hooks that
        ;; call (namespace agent-id).
        instance-id (or id
                        (when _deftool$id (generate-instance-id _deftool$id)))
        _ (when-not (keyword? instance-id)
            (mulog/error ::setup-agent-invalid-instance-id
                         :id id
                         :_deftool$id _deftool$id
                         :user-id user-id
                         :session-id session-id)
            (throw (ex-info
                    (str "setup-agent could not resolve a valid instance-id. "
                         "Pass :id (keyword) or invoke via a registered defagent "
                         "so :_deftool$id is supplied.")
                    {:id id
                     :_deftool$id _deftool$id
                     :user-id user-id
                     :session-id session-id})))
        ;; Ensure global config is loaded once (lazy + idempotent). Reads
        ;; .brainyard/config.edn :auto and merges over default-config.
        _ (config/load-global-config!)
        ;; Resolve max-iterations: explicit arg > defagent meta > schema default
        max-iterations (or max-iterations
                           (get config/default-config :max-iterations 100))
        options (assoc options :max-iterations max-iterations)
        ;; Bind tools if agent-tools present. Pass :agent-id so bind-tools can
        ;; drop tools hidden by :tool-use-control (visibility :hidden / allow /
        ;; deny patterns) — keeps the LLM JSON tool-calls roster in sync with
        ;; what's actually callable.
        [tools tools-fn-map] (when agent-tools
                               (tool/bind-tools (assoc agent-tools :agent-id instance-id)))
        ;; Build BT from factory
        bt-config (when bt-factory (bt-factory options))
        ;; Split :config-extra into schema-shape overrides (→ st-mem-init :config)
        ;; vs. non-schema agent metadata (→ @!state :config). Caller options that
        ;; collide with config-schema keys win over defagent author defaults.
        schema-overrides  (merge (select-keys config-extra config/config-keys)
                                 (select-keys options config/config-keys))
        agent-meta-extras (apply dissoc config-extra config/config-keys)
        ;; Build st-memory-init. `:config` is the canonical per-agent
        ;; override slot — readers use `config/get-config` to resolve.
        st-init (cond-> {:tool-context   tool-context
                         :instruction    instruction
                         :agent-context  agent-context
                         :config         schema-overrides}
                  tools        (assoc :tools tools)
                  tools-fn-map (assoc :tools-fn-map tools-fn-map))
        st-init (if st-memory-extra (merge st-init st-memory-extra) st-init)
        ;; @!state :meta — immutable agent identity set at creation.
        agent-meta {:name        (subs (str instance-id) 1)
                    :description _deftool$description
                    :agent-tools agent-tools}
        ;; @!state :config — non-schema agent-meta extras only
        ;; (e.g. :slack, :system-commands). Schema overrides live in
        ;; (:config @st-memory-init) and are read via config/get-config.
        agent-config agent-meta-extras
        ;; Create/retrieve agent
        create-opts (cond-> {:meta           agent-meta
                             :config         agent-config
                             :bt-config      bt-config
                             :st-memory-init st-init
                             :memory-opts    (:memory-opts options)
                             :parent-agent   parent-agent}
                      session-store (assoc :session-store session-store))]
    (get-or-create-agent user-id session-id instance-id create-opts)))

(defn setup-agent-by-id
  "Look up a registered defagent by its defagent-type, resolve its metadata, and
   create/start an instance. Returns the started Agent record.
   Does NOT call ask/ask-async.

   Resolves the defagent metadata from the tool registry, merges with caller
   options, and delegates to setup-agent. Use this from TUI/web entry points
   that need to customize the agent after creation.

   The instance-id defaults to (generate-instance-id agent-id), i.e.
   :<defagent-type>/<suffix>. Callers may override by passing an explicit :id
   (should also be a namespaced keyword, e.g. :coact-agent/my-run).

   Options (merged with defagent metadata):
     :id               - override instance-id (must be unique)
     :agent-session    - {:user-id :session-id} (required) — the agent-session
     :max-iterations   - override default
     :config-extra     - map merged into :config
     :st-memory-extra  - map merged into :st-memory-init
     :session-store    - custom session store"
  [agent-id & {:keys [id] :as options}]
  (let [def-entry (tool/get-tool-defs :id agent-id)
        _ (when-not def-entry
            (throw (ex-info (str "Agent not found in registry: " agent-id)
                            {:agent-id agent-id})))
        instance-id (or id (generate-instance-id agent-id))
        merged (merge (:meta def-entry) options {:id instance-id})]
    (apply setup-agent (mapcat identity merged))))

(defn- close-agent-quietly!
  "Close an agent without letting exceptions escape."
  [^java.io.Closeable ag]
  (try (.close ag) (catch Exception _)))

(defn- attach-auto-close-watch!
  "Attach a one-shot watch to a Clojure agent ref that closes the brainyard
   agent when the async ask completes (state transitions to a non-nil :output)."
  [clj-ref ag]
  (add-watch clj-ref ::auto-close
             (fn [k _ref old new]
               (when (and (nil? (:output old)) (some? (:output new)))
                 (close-agent-quietly! ag)
                 (remove-watch clj-ref k)))))

(defn run-agent
  "Generic agent runner — setup + ask. Used as the function in defagent registrations.
   For cases where you need the Agent record (to add watches, permissions, etc.),
   use setup-agent or setup-agent-by-id instead.

   By default, uses ask-async (returns clojure agent ref) when no parent-agent,
   and synchronous ask when parent-agent is set. Override with :ask-async? true
   to force async even with a parent-agent.

   Options:
     :setup-only? - Return the Agent record without calling ask. Useful for entry
                    points (TUI, web) that need to configure the agent (permissions,
                    watches) before the first ask. :auto-close? is ignored in this mode.
     :auto-close? - Close the agent after ask completes. For sync ask, closes in a
                    finally block. For async ask, attaches a one-shot watch that
                    closes when the clj-agent's :output transitions to non-nil.
                    Defaults to true for sub-agent calls (see do-call-tool--agent)."
  [& {:keys [question query parent-agent ask-async? setup-only? auto-close?] :as options}]
  (let [question (or question query "")]
    (when (and (not setup-only?)
               (or (nil? question) (and (string? question) (clojure.string/blank? question))))
      (throw (ex-info "run-agent requires a non-empty :question" {:question question})))
    (let [ag (apply setup-agent (mapcat identity (assoc options :question question)))]
      (cond
        setup-only?
        ag

        (or ask-async? (not parent-agent))
        (let [clj-ref (ask-async ag question)]
          (when auto-close? (attach-auto-close-watch! clj-ref ag))
          clj-ref)

        :else
        (try
          (ask ag question)
          (finally
            (when auto-close? (close-agent-quietly! ag))))))))
