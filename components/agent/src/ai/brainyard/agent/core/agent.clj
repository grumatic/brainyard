(defn ask
  "Ask the agent a question. Main entry point for agent interaction.

   Handles:
   1. System commands (/cancel, /new, etc.)
   2. Memory recall
   3. BT execution
   4. Memory remember

   Parameters:
     agent - Agent instance
     input - User's input string

   Returns: response map with :answer/:error/:usage, or :error on invalid input

   `opts` (optional, default {}) carries ask metadata. `:source :wakeup` marks
   an auto-ask injected by task$wakeup — the input is recorded as a system
   message (not a user question) and :agent.ask/pre carries :source so the
   capture pipeline / prompt can distinguish a resumed turn from a fresh one."
  [agent input & [{:keys [source] :as _opts}]]
  ;; Validate agent
  (when-not (and agent (:!state agent) (:!session agent))
    (throw (ex-info "ask requires a valid agent with !state and !session" {:agent-id (:agent-id agent)})))
  ;; Validate input — accept :query fallback for LLMs that use wrong key
  (let [input (or input "")]
    (when (and (string? input) (clojure.string/blank? input))
      (throw (ex-info "ask requires a non-empty input" {:agent-id (:agent-id agent) :input input}))))
  (let [!state (:!state agent)
        !session (:!session agent)]
    ;; Reset runtime for new run
    (runtime/reset-runtime !state)
    (swap! !state assoc :status :running)

    ;; Bump two counters per ask:
    ;;   - per-agent :turn-id (this agent's monotonic ask count — used for
    ;;     display, previous-turns, and per-agent observability)
    ;;   - session :total-turns (cumulative count across root + sub-agents
    ;;     in the session — used as the audit row index combined with
    ;;     :session-id and :agent-id)
    ;; Both are mirrored into st-memory-init so BT actions (coact-init,
    ;; recall, dspy-action) can read them.
    (let [total-turns (session/inc-total-turns! !session)]
      (when-let [st-init (:st-memory-init @!state)]
        (let [{:keys [turn-id]} (swap! st-init update :turn-id (fnil inc 0))]
          (swap! st-init assoc :total-turns total-turns)
          ;; Reference turn-id so the binding isn't dropped (some BT actions
          ;; read it from the post-swap value via st-init).
          turn-id)))

    ;; Clear progress data (root agent only — sub-agents share parent's !session)
    (let [parent (runtime/get-parent-agent !state)]
      (when-not parent
        (swap! !session session/clear-data))

      ;; Root agent: user message; sub-agent: assistant message tagged with
      ;; parent's id; wakeup auto-ask: system message (not a user question).
      (swap! !session session/add-message
             (cond
               parent             (session/assistant-message input (:agent-id parent))
               (= source :wakeup) (session/system-message input)
               :else              (session/user-message input))))

    ;; Pre-ask hook: after status :running, before process
    (hooks/fire! :agent.ask/pre {:agent agent :input input :source source})

    (llm/with-active-stream-register
      (fn [stream]
        (if stream
          (runtime/set-active-http! !state stream)
          (runtime/clear-active-http! !state)))
      (try
        (let [result (proto/process agent input nil)
            ;; Include usage summary if tracker exists
              usage-summary (when-let [tracker (session/get-session-config @!session :usage-tracker)]
                              (llm/get-usage-summary tracker))
              result (cond-> result
                       usage-summary (assoc :usage usage-summary))
            ;; Finalize hook (gated): a handler may :replace the result —
            ;; e.g. inject an absent `Saved <kind>: <path>` handoff line for an
            ;; auto-persisted artifact. Runs BEFORE the session message + the
            ;; observe-only :agent.ask/post so both see the finalized answer.
              result (let [d (hooks/fire-decision! :agent.ask/finalize
                                                   {:agent agent :input input :result result})]
                       (if (= :replace (:result d)) (:replacement d) result))]
        ;; Add assistant response to session
          (when-let [answer (:answer result)]
            (swap! !session session/add-message
                   (session/assistant-message answer (:agent-id agent))))

        ;; Post-ask hook: after process, before status :idle
          (hooks/fire! :agent.ask/post {:agent agent :input input :result result})

          (swap! !state assoc :status :idle)
          result)

        (catch Exception e
          (swap! !state assoc :status :idle)
          (mulog/error ::agent-execution-failed :agent-id (:agent-id agent) :exception e)
        ;; Surface to the memory capture pipeline (when enabled) so
        ;; failures join the L2 timeline alongside the user/assistant
        ;; messages that bracketed them.
          (try (hooks/fire! :agent/exception
                            {:agent agent :phase :ask :exception e})
               (catch Exception _ nil))
        ;; Also fire `:agent.ask/post` so listeners that close out the
        ;; ask cycle (daemon status snapshot, TUI iteration block
        ;; freeze, activity-pane footer) flip back to idle even on
        ;; failure.  Without this a user-initiated cancel mid-LLM-call
        ;; leaves the status bar stuck at "running" forever — the BT
        ;; threw `Cancelled`, the success-path hooks/fire! at line 563
        ;; never ran, so the daemon never saw an :ask/post and the
        ;; pane chrome stayed in :running mode.
          (let [iters (try
                        (some-> ^ai.brainyard.agent.core.protocol.IAgentBTIntegration agent
                                .get-bt-st-memory deref
                                ((some-fn :iteration :iteration-count)))
                        (catch Throwable _ nil))
                result (cond-> {:error (ex-message e)}
                         iters (assoc :iteration-count iters))]
            (try (hooks/fire! :agent.ask/post
                              {:agent agent :input input :result result})
                 (catch Exception _ nil))
            result))

        (finally
        ;; Notify parent's TUI that this sub-agent is done
          (when (runtime/get-parent-agent !state)
            (fire-agent-done-activity! agent)))))))

(defn ask-async
  "Ask the agent a question asynchronously using a Clojure agent.
   Dispatches (ask agent input) via send-off. Back-to-back calls are
   automatically queued and executed sequentially.
   Returns the Clojure agent ref. Use (await clj-ag) then (:output @clj-ag)
   to get the result, or add-watch for reactive notification.

   `opts` (default {}) is forwarded to `ask` — e.g. `{:source :wakeup}` for
   task$wakeup auto-asks. Back-to-back sends (user turn + wakeup) are queued
   and run sequentially by the Clojure agent, so a wakeup can never race a
   live turn."
  ([agent input] (ask-async agent input {}))
  ([agent input opts]
   (let [clj-ag (:clj-agent @(:!state agent))]
     (runtime/send-ask clj-ag ask agent input opts))))

(defonce ^{:doc "Host-registered turn submitter — see register-turn-submitter!."}
  !turn-submitter (atom nil))

(defn register-turn-submitter!
  "Register a host fn `(fn [agent input opts])` that submits a turn through the
   host's OWN single executor (e.g. the TUI input queue). Auto-asks injected by
   task$wakeup go through `submit-turn`, which uses this when set — so a wakeup
   serializes with user turns on the same queue instead of racing them on a
   separate executor. Unset (headless / single-executor host) → submit-turn
   falls back to ask-async."
  [f]
  (reset! !turn-submitter f))

(defn submit-turn
  "Submit `input` as a turn for `agent`, forwarding `opts` (e.g.
   {:source :wakeup}) to `ask`. Routes through the host turn-submitter when one
   is registered (serializes with user turns), else via ask-async."
  [agent input opts]
  (if-let [f @!turn-submitter]
    (f agent input opts)
    (ask-async agent input opts)))

(defn deliver-action
  "Deliver a user response to a pending action promise."
  [agent action-id value]
  (runtime/deliver-action-response (:!state agent) action-id value))

