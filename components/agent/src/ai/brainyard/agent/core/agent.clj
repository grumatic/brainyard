(defn- inject-resume-note!
  "When the agent was resumed from a pause with a user note, fold the note into
   the *running* loop so the next iteration's prompt carries it with directive
   force. We append to st-memory `:question` — the ACTIVE TASK that both the
   react and coact loops render as the primary objective each iteration — so
   the note actually steers the remaining work. (Appending to `:conversation`
   alone lands it in the auxiliary `## Conversation History` section, which the
   LLM treats as background and deprioritizes against the original question — a
   live test showed the agent ignoring it there.) A `:conversation` breadcrumb
   and a session trace are kept for visibility. No-op when there is no pending
   note or no `:question`. Called from `apply-resume-note!` at every BT
   checkpoint, so it fires whether or not the loop actually parked on the pause."
  [!state !session agent-id]
  (when-let [note (runtime/take-resume-note! !state)]
    (when-let [st (get-in @!state [:behavior-tree :context :st-memory])]
      (swap! st (fn [s]
                  (-> s
                      (update :question
                              (fn [q]
                                (str (when-not (clojure.string/blank? (str q)) (str q "\n\n"))
                                     "[MID-RUN STEERING — you were paused and the user resumed the "
                                     "iteration loop with this added/updated instruction; treat it as "
                                     "authoritative and adjust your remaining work now] " note)))
                      (update :conversation (fnil conj [])
                              {:role "user" :content (str "[resumed from pause] " note)})))))
    (when @!session
      (swap! !session session/update-data
             {:trace {:agent-id agent-id
                      :content (str "iteration loop **resumed** with user request: " note)}}))
    (mulog/info ::resumed-with-user-note :agent-id agent-id)))

(defrecord Agent [agent-id !state !session]

  ;; ---- IAgent ----
  proto/IAgent
  (agent-id [_] agent-id)
  (agent-name [_] (get-in @!state [:meta :name] agent-id))
  (agent-description [_] (get-in @!state [:meta :description] ""))
  (user-id [_] (some-> !session deref :user-id))
  (session-id [_] (some-> !session deref :session-id))
  (defagent-type [_]
    (if-let [ns (and (keyword? agent-id) (namespace agent-id))]
      (keyword ns)
      agent-id))
  (process [this input ctx]
    (let [state @!state
          ;; Check for system commands
          sys-cmd (context/process-system-command input)]
      (if sys-cmd
        ;; Handle system command
        (let [{:keys [command args]} sys-cmd
              handler (get-in state [:config :system-commands command])]
          (if handler
            (handler args this)
            {:error (str "Unknown command: /" command)}))
        ;; Normal processing
        (let [bt (:behavior-tree state)]
          (if bt
            ;; BT-based processing
            (let [result (agent-bt/run-bt this input)
                  st-mem (get-in @!state [:behavior-tree :context :st-memory])]
              {:result result
               :answer (some-> st-mem deref :answer)})
            ;; Fallback: direct LLM processing via tool loop
            (let [tools (proto/get-tools this)]
              {:result :no-bt
               :tools (count tools)
               :input input}))))))
  (get-tools [_]
    (or (some-> @!state :st-memory-init deref :tools) []))
  (get-state [_] @!state)

  ;; ---- IAgentLifecycle ----
  proto/IAgentLifecycle
  (start-agent [this]
    (swap! !state assoc :status :idle)
    (register-agent this)
    (mulog/info ::agent-started :agent-id agent-id)
    this)
  (stop-agent [this]
    (runtime/cancel-run !state)
    (swap! !state assoc :status :stopped)
    (unregister-agent agent-id)
    (mulog/info ::agent-stopped :agent-id agent-id)
    this)
  (agent-running? [_]
    (boolean (#{:idle :running :paused :cancelled} (:status @!state))))
  (clone-agent [this] (proto/clone-agent this {}))
  (clone-agent [this opts]
    (let [{:keys [st-memory-init-overrides bt-config parent-agent]} opts

          ;; Generate new agent-id as keyword. The clone prefix flattens the
          ;; source id into a single namespace segment so nested clones stay clean:
          ;;   "react-agent"   -> "react-agent"
          ;;   :react-agent    -> "react-agent"
          ;;   :skill/planner  -> "skill.planner"
          clone-prefix (cond
                         (string? agent-id) agent-id
                         (namespace agent-id) (str (namespace agent-id) "." (name agent-id))
                         :else (name agent-id))
          clone-id (keyword clone-prefix (util/gen-random-words))

          ;; Snapshot current state
          state @!state
          src-config (:config state)
          src-bt (:behavior-tree state)

          ;; Snapshot st-memory (deref the atom inside BT context)
          st-memory-snapshot (when-let [st-atom (get-in src-bt [:context :st-memory])]
                               @st-atom)

          ;; Snapshot + override st-memory-init (mark as clone for lifecycle hooks)
          src-st-memory-init (when-let [a (:st-memory-init state)] @a)
          merged-st-memory-init (merge-with (fn [old new]
                                              (if (and (map? old) (map? new))
                                                (merge old new)
                                                new))
                                            (or src-st-memory-init {})
                                            (assoc (or st-memory-init-overrides {})
                                                   :cloned? true))

          ;; Build new agent state. :meta (identity: name/description/
          ;; agent-tools) is inherited from the source so the clone keeps the
          ;; same name and tool bag — agent-name/agent-description read it.
          initial-state {:meta (:meta state)
                         :config src-config
                         :memory-manager (:memory-manager state)
                         :session-store (:session-store state)
                         :runtime (runtime/create-runtime-state)
                         :st-memory-init (atom merged-st-memory-init)
                         :status :created}
          new-!state (atom initial-state)

          ;; Create clone (shared !session)
          clone (->Agent clone-id new-!state !session)]

      ;; Parent: explicit override > source's parent
      (runtime/set-parent-agent new-!state
                                (or parent-agent (runtime/get-parent-agent !state)))

      ;; BT: rebuild from bt-config, or reuse source's compiled tree
      (if bt-config
        ;; Build fresh BT from provided config
        (try
          (let [new-bt (agent-bt/build-bt bt-config clone
                                          :st-memory-init merged-st-memory-init)]
            (swap! new-!state assoc :behavior-tree new-bt))
          (catch Exception e
            (mulog/error ::clone-bt-build-failed :clone-id clone-id :error (ex-message e))
            (throw (ex-info "Clone BT build failed" {:clone-id clone-id} e))))
        ;; Reuse source's compiled tree with fresh context
        (when src-bt
          (let [new-bt {:tree (:tree src-bt)
                        :context {:st-memory (atom (or st-memory-snapshot {}))
                                  :agent clone}}]
            (swap! new-!state assoc :behavior-tree new-bt))))

      ;; Start and register
      (proto/start-agent clone)
      ;; Forward display events to shared !session for TUI rendering
      (attach-activity-forward-watch! clone)
      (mulog/info ::agent-cloned :source-agent-id agent-id :clone-agent-id clone-id)
      (hooks/fire! :agent.instance/created {:agent clone})
      clone))

  ;; ---- IAgentState ----
  proto/IAgentState
  (get-state-value [_ path]
    (get-in @!state path))
  (set-state-value! [_ path value]
    (swap! !state assoc-in path value))
  (get-bt [_]
    (:behavior-tree @!state))
  (get-bt-context [_]
    (get-in @!state [:behavior-tree :context]))
  (get-st-memory-init [_]
    (:st-memory-init @!state))

  ;; ---- IAgentBTIntegration ----
  ;; These methods are called by BT extended nodes in bt.clj via (.method agent ...)
  proto/IAgentBTIntegration
  (update-session-data [_ data]
    (when @!session
      (swap! !session session/update-data data)))

  (check-run-cancelled? [_]
    (runtime/cancelled? !state))

  (check-run-paused? [_]
    (runtime/paused? !state))

  (await-resume [_]
    (runtime/wait-if-paused !state))

  (apply-resume-note! [_]
    (inject-resume-note! !state !session agent-id))

  (create-action-promise [_ action-id]
    (runtime/create-action-promise !state action-id))

  (get-action-permission [_ action-id]
    (runtime/get-action-permission !state action-id))

  (set-action-permission [_ action-id value]
    (runtime/set-action-permission !state action-id value))

  (get-bt-st-memory [_]
    (get-in @!state [:behavior-tree :context :st-memory]))

  ;; ---- IAgentMemoryAccess ----
  proto/IAgentMemoryAccess
  (get-memory-manager [_]
    (:memory-manager @!state))

  ;; ---- java.io.Closeable ----
  java.io.Closeable
  (close [this]
    (detach-activity-forward-watch! this)
    (hooks/fire! :agent.instance/closed {:agent this})
    (let [mm (:memory-manager @!state)]
      (proto/stop-agent this)
      ;; Release persistent sandbox reference for GC
      (swap! !state dissoc :sandbox)
      ;; Stop the memory capture pipeline only when this was the last
      ;; agent referencing the manager. proto/stop-agent already
      ;; unregistered us, so list-agents reflects the post-close state.
      (when (and mm (mem/capture-running? mm))
        (let [siblings (filter #(identical? mm (proto/get-memory-manager %))
                               (vals @!agent-registry))]
          (when (empty? siblings)
            (try (mem/stop-capture! mm)
                 (catch Exception e
                   (mulog/warn ::capture-stop-failed
                               :agent-id agent-id
                               :error (ex-message e))))))))
    (mulog/info ::agent-closed :agent-id agent-id)))

