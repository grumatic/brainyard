(ns ai.brainyard.agent.core.agent
  "Core Agent defrecord — the stateful agent entity.

   Agent is a defrecord implementing:
   - IAgent            — identity, processing, tools, state
   - IAgentLifecycle   — start/stop/running
   - IAgentState       — deep state access, BT accessors
   - IAgentBTIntegration — methods expected by BT extended nodes in bt.clj
   - IAgentMemoryAccess — memory manager access, recall/remember
   - java.io.Closeable — cleanup on close

   The agent holds:
   - !state atom with:
     {:config          {...}    ;; agent configuration
      :st-memory-init  <atom of initial st-memory map>
      :behavior-tree   {:tree ... :context {:st-memory <atom> ...}}
      :memory-manager  <MemoryManager>
      :session-store   <ISessionStore>
      :runtime         {:future ... :cancelled? ... :action-permissions ...}
      :clj-agent       <clojure.lang.Agent>  ;; for serialized async execution
      :status          :created} ;; :created, :idle, :running, :paused, :cancelled, :stopped
   - !session atom (shared across agents in the same agent-session;
     carries :user-id and :session-id — the Agent record does NOT hold these):
     {:session-id ... :user-id ... :config {...} :messages [...] :data {...}}"
  (:require [clojure.string]
            [ai.brainyard.mulog.interface :as mulog]
            [ai.brainyard.agent.core.protocol :as proto]
            [ai.brainyard.agent.core.session :as session]
            [ai.brainyard.agent.core.runtime :as runtime]
            [ai.brainyard.agent.core.memory :as agent-mem]
            [ai.brainyard.agent.core.bt :as agent-bt]
            [ai.brainyard.agent.core.context :as context]
            [ai.brainyard.agent.core.tool :as tool]
            [ai.brainyard.agent.core.config :as config]
            [ai.brainyard.agent.core.hooks :as hooks]
            [ai.brainyard.clj-llm.interface :as llm]
            [ai.brainyard.memory.interface :as mem]
            [ai.brainyard.util.interface :as util]))

;; ============================================================================
;; Agent Registry (flat, keyed by globally-unique agent-id)
;;
;; Agent-id keys follow the convention :<defagent-type>/<suffix>, e.g.
;; :coact-agent/crimson-parrot-42. Uniqueness is enforced by the suffix
;; (generated by `generate-instance-id`). The defagent-type is recoverable
;; from any instance-id via (namespace agent-id) or proto/defagent-type.
;;
;; `!session` carries user-id / session-id — the registry does not.
;; ============================================================================

(defonce ^:private !agent-registry (atom {}))
(defonce ^:private !agent-creation-locks (atom {}))

(defn- get-or-create-lock
  "Get or create a per-key lock object for agent creation serialization."
  [key]
  (let [lock (Object.)]
    (-> (swap! !agent-creation-locks
               (fn [m] (if (contains? m key) m (assoc m key lock))))
        (get key))))

(defn generate-instance-id
  "Generate a unique agent instance-id in :<defagent-type>/<suffix> format.
   defagent-type must be a keyword naming a registered defagent."
  [defagent-type]
  {:pre [(keyword? defagent-type)]}
  (keyword (name defagent-type) (util/gen-random-words)))

(defn register-agent
  "Register an agent under its agent-id."
  [agent]
  (let [aid (proto/agent-id agent)]
    (swap! !agent-registry assoc aid agent)
    (mulog/info ::agent-registered :agent-id aid)
    agent))

(defn unregister-agent
  "Unregister an agent by agent-id."
  [agent-id]
  (swap! !agent-registry dissoc agent-id)
  (mulog/info ::agent-unregistered :agent-id agent-id))

(defn get-agent
  "Get an agent by its globally-unique agent-id."
  [agent-id]
  (get @!agent-registry agent-id))

(defn list-agents
  "List all registered agents."
  []
  (vals @!agent-registry))

(defn list-agents-for-session
  "List all agents belonging to a given agent-session-id."
  [session-id]
  (->> (vals @!agent-registry)
       (filter #(= session-id (proto/session-id %)))
       vec))

(defn reset-agent-registry!
  "Reset the agent registry and creation locks. For testing — clears all registered agents."
  []
  (reset! !agent-registry {})
  (reset! !agent-creation-locks {}))

