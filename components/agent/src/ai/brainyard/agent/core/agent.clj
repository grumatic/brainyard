(defn- attach-activity-forward-watch!
  "Attach a watch on a sub-agent's st-memory that forwards display-stage
   changes to the shared !session :agent-activity vector.
   Only attaches when the agent has a parent (i.e., is a sub-agent)."
  [agent]
  (let [!state  (:!state agent)
        !sess   (:!session agent)
        st-atom (get-in @!state [:behavior-tree :context :st-memory])
        ag-id   (keyword (:agent-id agent))
        ag-name (or (get-in @!state [:meta :name]) (:agent-id agent))]
    (when (and st-atom !sess (runtime/get-parent-agent !state))
      (add-watch st-atom ::forward-activity
                 (fn [_ _ old new]
                   (let [stage (:display-stage new)]
                     (when (and (some? stage)
                                (not= (:display-stage old) stage))
                       (let [data (case stage
                                   ;; Tool-results: forward only the delta
                                    :tool-results
                                    (let [old-r (or (:tool-results old) [])
                                          new-r (or (:tool-results new) [])]
                                      {:tool-results (if (> (count new-r) (count old-r))
                                                       (subvec new-r (count old-r))
                                                       new-r)})
                                   ;; All other stages: snapshot relevant keys
                                    (select-keys new
                                                 [:iteration-count :last-reasoning
                                                  :tool-calls :tool-results
                                                  :observation :goal-achieved :goal-reasoning
                                                  :eval-display :todo-list :answer
                                                  :terminated :iterations-exhausted
                                                  :question]))]
                         (swap! !sess session/append-agent-activity
                                {:agent-id   ag-id
                                 :agent-name (if (keyword? ag-name) (name ag-name) (str ag-name))
                                 :stage      stage
                                 :data       data
                                 :timestamp  (System/currentTimeMillis)})))))))))

(defn- detach-activity-forward-watch!
  "Remove the forwarding watch from a sub-agent's st-memory."
  [agent]
  (when-let [st-atom (get-in @(:!state agent) [:behavior-tree :context :st-memory])]
    (remove-watch st-atom ::forward-activity)))

(defn- fire-agent-done-activity!
  "Send an :agent-done activity event to the shared session.
   Called when a sub-agent's ask completes (success or failure)."
  [agent]
  (let [!state  (:!state agent)
        !sess   (:!session agent)
        ag-id   (keyword (:agent-id agent))
        ag-name (or (get-in @!state [:meta :name]) (:agent-id agent))]
    (when (and !sess (runtime/get-parent-agent !state))
      (swap! !sess session/append-agent-activity
             {:agent-id   ag-id
              :agent-name (if (keyword? ag-name) (name ag-name) (str ag-name))
              :stage      :agent-done
              :data       {}
              :timestamp  (System/currentTimeMillis)}))))

