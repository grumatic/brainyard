(defn- get-or-create-agent
  "Get an existing agent instance (by globally-unique agent-id) or create a new one.
   Uses double-check locking to prevent duplicate creation under concurrency.
   Internal — call setup-agent instead.

   Parameters:
     user-id    - User identifier (used to init the agent-session if new)
     session-id - Agent-session identifier (used to init the agent-session if new)
     agent-id   - Globally-unique instance id (:<defagent-type>/<suffix>)
     options    - Map of create-agent options (same keys: :config :bt-config
                  :st-memory-init :memory-opts :parent-agent etc.)

   Returns: Agent instance (started)"
  [user-id session-id agent-id options]
  (or (get-agent agent-id)
      (let [lock (get-or-create-lock agent-id)]
        (locking lock
          ;; Double-check after acquiring lock
          (or (get-agent agent-id)
              (let [agent (apply create-agent user-id session-id agent-id
                                 (mapcat identity options))]
                (proto/start-agent agent)
                agent))))))

