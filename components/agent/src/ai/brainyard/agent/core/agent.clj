(defn- create-agent
  "Create a new Agent instance. Internal — call setup-agent instead.

   Parameters:
     user-id    - User identifier (stored on the agent-session, not the record)
     session-id - Agent-session identifier (stored on the agent-session)
     agent-id   - Globally-unique instance id, conventionally :<defagent-type>/<suffix>

   Options:
     :meta           - Agent identity map {:name :description :agent-tools} set
                       at creation; readers use `(get-in @!state [:meta :name])`
                       and `proto/agent-name` / `proto/agent-description`.
     :config         - Non-schema agent-meta-extras (e.g. :slack, :system-commands).
                       Schema-shape overrides live in `(:config @st-memory-init)`
                       and are read via `config/get-config`.
     :bt-config      - Behavior tree configuration vector
     :memory-manager - Pre-built memory manager (skips auto-creation)
     :memory-opts    - Options for memory manager creation {:in-memory :base-path :db-path}
                       Defaults to in-memory when not specified.
     :session-store  - ISessionStore instance for session persistence
     :parent-agent   - Parent agent for sub-agent hierarchy (inherits !session;
                       user-id/session-id args are ignored in that case)
     :st-memory-init - Initial short-term memory map (stored as atom in !state)

   Returns: Agent instance (not yet started)"
  [user-id session-id agent-id
   & {:keys [meta config bt-config memory-manager memory-opts
             session-store parent-agent st-memory-init]
      :or {meta {} config {} memory-opts {} st-memory-init {}}}]

  ;; Create or use provided memory manager (always created, defaults to in-memory)
  (let [mm (or memory-manager
               (agent-mem/create-memory-manager user-id
                                                :in-memory (:in-memory memory-opts)
                                                :base-path (:base-path memory-opts)
                                                :db-path (:db-path memory-opts)))

        ;; Session initialization priority:
        ;; 1. parent-agent's session (sub-agents always inherit)
        ;; 2. get-or-create from session store (shared with other agents/UI)
        ;; 3. standalone atom (no store)
        !session (if parent-agent
                   (:!session parent-agent)
                   (session/get-or-create-session
                    session-store session-id user-id
                    :config (:session-config config)))

        ;; Ensure session has a usage tracker before agent creation
        ;; (idempotent — skips if parent already created one)
        _ (when-not (session/get-session-config @!session :usage-tracker)
            (swap! !session session/set-session-config :usage-tracker (llm/create-usage-tracker)))

        ;; Build initial state — fully populated before ->Agent construction
        runtime-state (cond-> (runtime/create-runtime-state)
                        parent-agent (assoc :parent-agent parent-agent))

        ;; Lifecycle hooks are now global (see core.hooks); no parent->child
        ;; propagation needed because a single registered handler sees both
        ;; the parent and the sub-agent automatically. Use match predicates
        ;; (match-root-agent, match-defagent-type) when scoping is required.

        initial-state {:meta meta
                       :config config
                       :memory-manager mm
                       :session-store session-store
                       :runtime runtime-state
                       :st-memory-init (atom st-memory-init)
                       :status :created}

        !state (atom initial-state)

        ;; Create the agent record (user-id / session-id live on !session,
        ;; accessed via proto/user-id and proto/session-id).
        agent (->Agent agent-id !state !session)

        ;; Build BT if config provided (needs agent ref for context)
        _ (when bt-config
            (let [bt (agent-bt/build-bt bt-config agent
                                        :st-memory-init st-memory-init)]
              (swap! !state assoc :behavior-tree bt)))

        ;; Create Clojure agent for serialized async execution (needs agent ref)
        _ (swap! !state assoc :clj-agent (runtime/create-clj-agent agent))

        ;; Forward sub-agent display events to shared !session for TUI rendering
        _ (when parent-agent
            (attach-activity-forward-watch! agent))

        ;; Start the memory capture pipeline when enabled. The flag may
        ;; arrive on:
        ;;   - `:config` (low-level callers using @!state :config)
        ;;   - `(:config st-memory-init)` (unified per-agent override slot
        ;;     populated by setup-agent from defagent :config-extra schema
        ;;     keys + caller options).
        ;; start-capture! is idempotent per-manager, so calling it for
        ;; every agent that shares the same manager is safe.
        _ (when (and mm
                     (or (:enable-memory-capture config)
                         (:enable-memory-capture (:config st-memory-init))))
            (try (mem/start-capture! mm)
                 (catch Exception e
                   (mulog/warn ::capture-start-failed
                               :agent-id agent-id
                               :error (ex-message e)))))]

    (mulog/info ::agent-created :agent-id agent-id :user-id user-id :session-id session-id)
    (hooks/fire! :agent.instance/created {:agent agent})
    agent))

